    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        String bloodGroup = (String) jComboBox1.getSelectedItem();
        int units =Integer.parseInt( jTextField1.getText());

        try {
            Connection con = connectionProvider.getCon();
            Statement st = con.createStatement();
            st.executeUpdate("update stocks set units = units - '"+units+"' where bloodGroup = '"+bloodGroup+"' ");
            JOptionPane.showMessageDialog(null, "Updated Successfully!");
            setVisible(false);

            new stockDecrease().setVisible(true);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

